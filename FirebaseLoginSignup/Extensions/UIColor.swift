import UIKit

extension UIColor {
    
    //The bg color for our theme
    static var backgroundColor: UIColor {
        return UIColor(red: 1, green: 1, blue: 1, alpha: 1)
    }
    
    //Our secondary color
    static var secondaryColor: UIColor {
        return UIColor.orange
    }
}
